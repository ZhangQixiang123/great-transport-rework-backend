def _parse_persona_output(text: str) -> tuple[str, str]:
    """Parse freeform persona output into (title, description).

    Expected format:
        标题：<title text>
        简介：<description text>

    Falls back to JSON parsing if delimiter format not found.
    """
    text = text.strip()

    # Try delimiter format first
    title_match = re.search(r"标题[：:][^\S\n]*(.+?)(?:\n|$)", text)
    desc_match = re.search(r"简介[：:][^\S\n]*(.*)", text, re.DOTALL)

    if title_match and desc_match:
        return title_match.group(1).strip(), desc_match.group(1).strip()

    # Fallback: try JSON parsing
    try:
        result = json.loads(text)
        return result.get("title", "").strip(), result.get("description", "").strip()
    except (json.JSONDecodeError, AttributeError):
        pass

    return "", ""


def generate_persona_copy(
    backend: Optional[_LLMBackend],
    video_info: Dict[str, Any],
    strategy_name: str = "",
) -> Dict[str, str]:
    """Generate persona-driven title and description in one LLM call.

    Returns dict with 'title' and 'description' keys.
    The description includes the standard YouTube template footer.
    Falls back gracefully on any failure.
    """
    if backend is None:
        return {
            "title": video_info.get("title", ""),
            "description": fallback_description(video_info),
        }

    title = video_info.get("title", "Unknown")
    channel = video_info.get("channel", "Unknown")
    views = video_info.get("view_count", 0)
    duration = video_info.get("duration_seconds", 0)
    category_id = video_info.get("category_id", 0)
    category_name = _YT_CATEGORY_NAMES.get(category_id, "其他")

    view_str = format_view_count(views)

    hint = STRATEGY_HINTS.get(strategy_name, "")
    strategy_line = f"\n搜索策略：{strategy_name}\n策略提示：{hint}" if hint else ""

    user_content = (
        f"原标题：{title}\n"
        f"频道：{channel}\n"
        f"YouTube播放量：{view_str}\n"
        f"时长：{duration // 60}分{duration % 60}秒\n"
        f"分类：{category_name}"
        f"{strategy_line}"
    )

    sampled = random.sample(_PERSONA_EXAMPLES, 3)
    messages = [{"role": "system", "content": PERSONA_SYSTEM_PROMPT}]
    for ex in sampled:
        messages.append({"role": "user", "content": ex["input"]})
        messages.append({"role": "assistant", "content": ex["output"]})
    messages.append({"role": "user", "content": user_content})

    try:
        response = backend.chat(
            messages=messages,
            temperature=1.1,
        )
        gen_title, gen_desc = _parse_persona_output(response)

        if not gen_title or len(gen_title) > 80:
            logger.warning("Persona title empty or >80 chars, falling back")
            return {
                "title": video_info.get("title", ""),
                "description": fallback_description(video_info),
            }

        # Bare link for attribution; source/view-count mention is in the LLM text
        video_id = video_info.get("video_id", "")
        link_footer = f"\n\n原视频：https://www.youtube.com/watch?v={video_id}" if video_id else ""
        full_desc = gen_desc + link_footer if gen_desc else fallback_description(video_info)

        return {"title": gen_title, "description": full_desc}

    except Exception as e:
        logger.warning("Persona copy generation failed: %s", e)
        return {
            "title": video_info.get("title", ""),
            "description": fallback_description(video_info),
        }
